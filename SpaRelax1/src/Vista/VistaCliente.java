      private void cargarTabla() {
          modelo.setRowCount(0);

    List<Cliente> listarClientes = cliente.listarClientes();

    for (Cliente c : listarClientes) {
        String estadoApto;

        if (c.isEstado()== true) {
            estadoApto = "SÃ­";
        } else {
            estadoApto = "No";
        }

        modelo.addRow(new Object[]{
            c.getDni(),
            c.getNombreCompleto(),
            c.getTelefono(),
                c.getEdad(),
                c.getAfecciones(),
                c.isEstado()
        });
        jTable1.setModel(modelo);
      }
    }
    
                                     

